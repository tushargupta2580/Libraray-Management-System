    void populateTable()
    {
        try
        {
            String cols[]={"id","title","Author","publisher","price"};
            String qry="select count(*)NOR from tbbook";
            DB obj=new DB();
            ResultSet rs=obj.execute(qry);
            rs.next();
            int rows=rs.getInt("NOR");
            String data[][]=new String[rows][5];
            qry = "select * from tbbook";
            rs=obj.execute(qry);
            int i=0,j=0;
            
            while(rs.next())
            {
                data[i][j++]=rs.getString("bookid");
                data[i][j++]=rs.getString("booktit");
                data[i][j++]=rs.getString("bookaut");
                data[i][j++]=rs.getString("bookpub");
                data[i][j++]=rs.getString("bookprc");
                
                j=0;
                i++;
            }
            DefaultTableModel dtm=new DefaultTableModel(data,cols);
            jTable1.setModel(dtm);
                    
            
            
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }
